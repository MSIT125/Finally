        //
        // POST: /Account/Login
        [HttpPost]
        [CaptchaValidation("CaptchaCode", "ExampleCaptcha", "驗證碼輸入錯誤!")]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl, bool captchaValid)
        {
            // Require the user to have a confirmed email before they can log on.
            //用戶的信箱必須要驗證才能登入
            var user = await UserManager.FindByNameAsync(model.Email);
            if (user != null)
            {
                if (!await UserManager.IsEmailConfirmedAsync(user.Id))
                {
                    ViewBag.errorMessage = "您必須要有認證過的信箱才能登入";
                    return View("Error");
                }
            }

            //驗證碼打錯要給錯誤訊息
            if (!ModelState.IsValid)
            {
                //Captcha validation failed, show error message
                TempData["ValidateError"] = "驗證碼錯誤";
                return View(model);
            }

            // 這不會計算為帳戶鎖定的登入失敗
            // 若要啟用密碼失敗來觸發帳戶鎖定，請變更為 shouldLockout: true
            var result = await SignInManager.PasswordSignInAsync(model.Email, model.Password, model.RememberMe, shouldLockout: false);
            switch (result)
            {
                case SignInStatus.Success:
                    return RedirectToLocal(returnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.RequiresVerification:
                    return RedirectToAction("SendCode", new { ReturnUrl = returnUrl, RememberMe = model.RememberMe });
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "登入嘗試失試。");
                    return View(model);
            }
        }


